package com.faisal.herukun.listview01;

public class Player {

    private String name;
    private int age;
    private int photoId;


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getPhoto() {
        return photoId;
    }

    public void setPhoto(int photo) {
        this.photoId = photo;
    }
}
